namespace Magedelight\Giftcard\Controller\Adminhtml\Codes;

/**
 * Class Delete
 *
 * @package Magedelight\Giftcard\Controller\Adminhtml\Codes
 */
class Delete extends \Magento\Backend\App\Action
{
    /**
     * Authorization level of a basic admin session
     *
     * @see _isAllowed()
     */
    const ADMIN_RESOURCE = 'Magedelight_Giftcard::codes_delete';

    /**
     * Delete action
     *
     * @return \Magento\Backend\Model\View\Result\Redirect
     */
    public function execute()
    {
        // check if we know what should be deleted
        $id = $this->getRequest()->getParam('code_id');
        /** @var \Magento\Backend\Model\View\Result\Redirect $resultRedirect */
        $resultRedirect = $this->resultRedirectFactory->create();
        if ($id) {
            $title = "";
            try {
                // init model and delete
                $model = $this->_objectManager->create('Magedelight\Giftcard\Model\Code');
                
                $model->load($id);
                
                $title = $model->getCode();
                
                $model->delete($id);
                
                // display success message
                $this->messageManager->addSuccess(__('The code has been deleted.'));
                // go to grid
                $this->_eventManager->dispatch(
                    'adminhtml_giftcard_on_delete',
                    ['title' => $title, 'status' => 'success']
                );
                return $resultRedirect->setPath('*/*/');
            } catch (\Exception $e) {
                $this->_eventManager->dispatch(
                    'adminhtml_giftcard_on_delete',
                    ['title' => $title, 'status' => 'fail']
                );
                // display error message
                $this->messageManager->addError($e->getMessage());
                // go back to edit form
                return $resultRedirect->setPath('*/*/edit', ['code_id' => $id]);
            }
        }
        // display error message
        $this->messageManager->addError(__('We can\'t find a code to delete.'));
        // go to grid
        return $resultRedirect->setPath('*/*/');
    }
}